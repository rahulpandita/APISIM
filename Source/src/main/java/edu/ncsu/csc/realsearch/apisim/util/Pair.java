package edu.ncsu.csc.realsearch.apisim.util;

public class Pair<T,U>{
	private T first;
	
	private U Second;
	
	public T getFirst() {
		return first;
	}
	public void setFirst(T first) {
		this.first = first;
	}
	public U getSecond() {
		return Second;
	}
	public void setSecond(U second) {
		Second = second;
	}
}
