package edu.ncsu.csc.realsearch.apisim.databeans;

public class SearchResultBean {
	
	public String pkgName;
	
	public String clsName;
	
	public String clsNameCCSplit;
	
	public String mtdName;
	
	public String mtdNameCCSplit;
	
	public String mtdDesc;
	
	public String score;

	public String getPkgName() {
		return pkgName;
	}

	public void setPkgName(String pkgName) {
		this.pkgName = pkgName;
	}

	public String getClsName() {
		return clsName;
	}

	public void setClsName(String clsName) {
		this.clsName = clsName;
	}

	public String getClsNameCCSplit() {
		return clsNameCCSplit;
	}

	public void setClsNameCCSplit(String clsNameCCSplit) {
		this.clsNameCCSplit = clsNameCCSplit;
	}

	public String getMtdName() {
		return mtdName;
	}

	public void setMtdName(String mtdName) {
		this.mtdName = mtdName;
	}

	public String getMtdNameCCSplit() {
		return mtdNameCCSplit;
	}

	public void setMtdNameCCSplit(String mtdNameCCSplit) {
		this.mtdNameCCSplit = mtdNameCCSplit;
	}

	public String getMtdDesc() {
		return mtdDesc;
	}

	public void setMtdDesc(String mtdDesc) {
		this.mtdDesc = mtdDesc;
	}

	public String getScore() {
		return score;
	}

	public void setScore(String score) {
		this.score = score;
	}

}
